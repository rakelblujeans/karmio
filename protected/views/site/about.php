<h3 class="ARegTxt">About Us</h3>


<p>Welcome to the greatest business-led consumer empowerment movement on the web.<br />
  Karm.io brings you amazing deals that benefit local businesses and global nonprofits. Each purchase helps a cause—from providing clean water to a village halfway around the world, to creating a community garden right in your own backyard. You save money, support a local business, and help an organization working to build a better world. It&rsquo;s a good deal for everyone. </p>

 

<h1>How it works.</h1>
<p>First, a local merchant sets up a deal. Unlike other online deal sites, the merchant has
  total control over the terms, setting price, quantity, and timeframe. For this flexibility,
  the merchant agrees to donate a portion of each sale (minimum $1) to a nonprofit of
  their choosing. Karmio's staff reviews each submission and posts it to the homepage.</p>
<p>Other Karmio users see the deal, like it, and buy it. They receive a printable coupon,
  which they will use to redeem the deal. Their purchase is immediately split into two:<br />
  one portion going to Karmio for distribution to the merchant, and the other going to<br />
  Network for Good, which distributes the funds to the selected nonprofit. The donation<br />
  happens instantaneously, so consumers don’t have to worry about their money getting<br />
  to the nonprofit. After the deal ends, Karmio distributes the remaining sum to the<br />
  merchant.</p>
<p>Merchants reach new customers and grow their business, consumers get amazing deals<br />
  on great local services, and nonprofits get a new batch of donors and the funding they<br />
  need to change the world.</p>
<p>Everybody wins.</p>

