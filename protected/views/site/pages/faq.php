<p>
<a name="q19" class="qblue">** IF YOU ARE ENABLING RECURRING DONATIONS VIA CARD ON FILE **<br />How do recurring donations work on this site?</a><br />
If you opt to keep your credit card on file, you authorize our processing partner Network for Good to charge your credit card on a predetermined schedule (monthly, quarterly or annually) to process a recurring donation to your selected charity.  Network for Good, a <a target="_blank" href="http://www.bbb.org/charity-reviews/national/network-for-good-in-washington-dc-1290" style="color:#0000FF">Better Business Bureau Wise Giving Alliance</a> accredited 501c3 charity and donor advised fund, does not store your credit card number - we only store an authorization code that verifies the recurring transaction.  Network for Good uses industry-leading Secure Sockets Layer (SSL) technology to keep your personal information as secure as possible and protect against the loss, misuse and alteration of the information under our control. Network for Good cannot refund or modify donations once they are charged to your credit card, so please make any changes before the scheduled date of your next recurring donation.
</p>
