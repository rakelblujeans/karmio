<a name="q17" class="qblue">When will a charity receive my donation?</a><br />
Network for Good sends payments to nonprofits on the 15th of each month that include all donations made in the previous month. For example, donations processed in February will be paid on March 15th. 
</p>
<a href="#top" class="qbtop">Back to Top</a>

<p>
<a name="q18" class="qblue">How will my nonprofit organization receive funds from donations made through this site? </a><br />
Donations to US nonprofits are processed by our nonprofit partner Network for Good (<a target="_blank" style="color:#0000FF" href="http://www1.networkforgood.org/allaboutnfg">http://www1.networkforgood.org/allaboutnfg</a>); an industry leader in processing online donations with a charitable giving system that is approved by the Better Business Bureau Wise Giving Alliance (BBB). Payment for donations made to your nonprofit are sent on the 15th of each month and will include all donations made in the previous month. For example, donations processed in February will be paid on March 15th. Checks will be sent to your nonprofit's address as listed in <a href="http://www2.guidestar.org/" style="color:#0000FF">GuideStar</a> and will include an insert describing how to access the donation information (including the source) of all donations processed by Network for Good.
<ul style="color:#000000;" type="circle">
<li><p>To change the address listed for your nonprofit profile in GuideStar click here: <a target="_blank" style="color:#0000FF" href="https://commerce.guidestar.org/GuideStar/newaccount.aspx/">https://commerce.guidestar.org/GuideStar/newaccount.aspx/</a> .Updates to that form will be shared with Network for Good for future payments.</p></li></li>
<li><p>To access donor information, you can login to Network for Good's donation tracking report at <a target="_blank" href="https://www.networkforgood.org/npo/reports/" style="color:#0000FF">https://www.networkforgood.org/npo/reports/</a> You will need to establish a login using your charity's nine-digit EIN number. Once you are registered, you will have access to a real-time online report detailing information about your donors who have chosen to share their contact information. Network for Good encourages you to reach out to them directly and provide information about how their donation is being used to support your mission. </p>
</li>
<li><p>To get your donations more quickly (and to save paper!), have them sent to you electronically via electronic funds transfer, also called EFT. You can sign up here: <a target="_blank" href="https://www.networkforgood.org/npo/fundraising/eft/application1.aspx/" style="color:#0000FF">https://www.networkforgood.org/npo/fundraising/eft/application1.aspx/</a></p></li>
</ul>
</p>
