<p>
<a name="q10" class="qblue">Who is Network for Good?</a><br />
Network for Good is a nonprofit social enterprise that empowers nonprofits and corporate partners to unleash generosity and advance good causes. Network for Good helps nonprofits raise funds for their missions through simple, affordable and effective online fundraising services and offers free training through our online learning center (<a href="http://www.Fundraising123.org" style="color:#0000FF" target="_blank">www.Fundraising123.org</a>), interactive online community (<a href="http://www.TheNetworkforGood.org" style="color:#0000FF" target="_blank">www.TheNetworkforGood.org</a>) and webinar series (<a href="http://www.Nonprofit911.org" style="color:#0000FF" target="_blank">www.Nonprofit911.org</a>).  We also work with companies to help refine a cause strategy perfectly suited to business and philanthropic goals and implement effective cause initiatives powered by our proven donation platform.  Network for Good has processed more than $815 million in donations for more than 85,000 nonprofits since our 2001 founding by AOL, Cisco and Yahoo!. <a href="http://www.networkforgood.org" style="color:#0000FF" target="_blank">www.networkforgood.org</a>
</p>
<a href="#top" class="qbtop">Back to Top</a>

<p>
<a name="q11" class="qblue">How are donations processed?</a><br />
Donations are processed by our nonprofit partner Network for Good, a <a href="http://www.bbb.org/charity-reviews/national/network-for-good-in-washington-dc-1290" style="color:#0000FF" target="_blank">Better Business Bureau Wise Giving Alliance</a> accredited industry leader in processing online donations. Network for Good, in technical terms, is a "donor-advised fund," a 501(c)(3) organization that collects contributions from donors and distributes such contributions to 501(c)(3) organizations in good standing with the IRS recommended by you, the donor.
</p>
