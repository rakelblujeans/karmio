<a href="#top" class="qrtop">Back to Top</a>

<p>
<a name="q4" class="qred">What if I lose/forget my password?</a><br />
Karm.io does not store any passwords, so we can't send you a reminder. But resetting your password is easy.  Just visit the Karm.io site, click forgot my password and you'll get an email with instructions on how to create a new password.  
</p>
<a href="#top" class="qrtop">Back to Top</a>

