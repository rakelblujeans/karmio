<p>
<a name="q13" class="qblue">How do I know that my donation is secure?</a><br />
Network for Good uses industry-leading Secure Sockets Layer (SSL) technology to keep your personal information as secure as possible. Network for Good helps to protect your information by working with partners that provide a secure and safe environment for credit card donations.
<br /><br />
Furthermore, Network for Good, who processes the US credit card donations, is a <a href="http://www.bbb.org/charity-reviews/national/network-for-good-in-washington-dc-1290" style="color:#0000FF" target="_blank">Better Business Bureau Wise Giving Alliance</a> accredited charity that meets all of the BBB standards for charity accountability (BBB). They have processed more than $400 million in donations for 50,000 charities through their giving system.
</p>
<a href="#top" class="qbtop">Back to Top</a>

<p>
<a name="q14" class="qblue">How Does Network for Good protect my privacy?</a><br />
Network for Good respects and protects your privacy. Network for Good will never sell, trade or rent your personal information to other individuals or companies. The information that you provide is used only to complete your donation. With your permission, your name and contact information will be provided to the charity you supported through Network for Good. If you do not wish share your name and contact information with the charity you supported through Network for Good or you choose to make your donation anonymously, Network for Good will not share your name or contact information.
<br /><br />
Network for Good's website uses industry standard security measures to protect against the loss, misuse and alteration of the information under its control. All information is stored in a secure database.  You can read their privacy policy here: <a href="http://www1.networkforgood.org/privacy" target="_blank" style="color:#0000FF">http://www1.networkforgood.org/privacy </a>
</p>
<a href="#top" class="qbtop">Back to Top</a>

<p>
<a name="q15" class="qblue">Is my donation tax deductible?</a><br />
Your donation to a 501(c)3 charitable organization is 100% tax deductible. 
<br /><br />
Network for Good provides an electronic receipt at the time of the donation. Both the receipt you see onscreen at the completion of the donation process and the e-mail you receive shortly thereafter meet all IRS requirements as a record of donation. If you are asked to provide a paper receipt for IRS purposes, please print out a copy of your email receipt or send a request for a duplicate receipt to <a href="mailto:donationsupport@networkforgood.org">donationsupport@networkforgood.org</a>.
</p>
<a href="#top" class="qbtop">Back to Top</a>

<p>
<a name="q16" class="qblue">Are there costs to making a donation through this site?</a><br />
Your donation is being processed by Network for Good, a <a href="http://www.bbb.org/charity-reviews/national/network-for-good-in-washington-dc-1290" target="_blank" style="color:#0000FF;">Better Business Bureau Wise Giving Alliance</a> accredited nonprofit 501(c)3 donor advised fund. Network for Good re-grants the donation funds to your designated charity(ies), but there are costs associated with processing and distributing online donations to your favorite charity(ies).  A 4.75% grant is made to Network for Good with each donation transaction to fund its mission and help support the expense of maintaining the technology to process and distribute your donations.
</p>
