<p><strong>5. Cookies and Related Technologies</strong></p>

  <p>When you use our Sites, we collect certain information by automated or electronic means, using technologies such as cookies, browser analysis tools, and web server logs. As you use our Sites and applications, your browser and devices communicate with servers operated by us, our business partners and services providers to coordinate and record the interactivity and fill your requests for services and information.</p>

<p>The information from cookies and related technology is stored in web server logs and also in web cookies kept on your computers or mobile devices, which are then transmitted back to our Sites by your computers or mobile devices. These servers are operated and the cookies managed by us, our business partners or our service providers.</p>

<p>For example, when you visit our Sites, Karm.io and our service providers may place cookies on your computers or mobile devices. Cookies allow us to recognize you when you return, track and target your interests in order to provide a customized experience. They also help us provide a customized experience and help us to detect certain kinds of fraud. A &quot;cookie&quot; is a small amount of information that a web server sends to your browser that stores information about your account, your preferences, and your use of the Sites. Some cookies contain serial numbers that allow us to connect your Site activity with other information we store about you in your profile or as related to your interactions with the Sites.</p>

