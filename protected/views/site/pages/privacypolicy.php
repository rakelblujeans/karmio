<p>We collect many different types of information from cookies and other technologies. For example, we may collect information from the devices you use to access our Sites, your operating system type or mobile device model, browser type, domain, and other system settings, as well as the language your system uses and the country and time zone of your device. Browsers routinely send these types of information to web servers. Our server logs also record the Internet Protocol (&quot;IP&quot;) addresses of the devices you use to interact with the Sites. An IP address is a unique identifier that devices use to identify and communicate with each other on the Internet. We may also collect information about the website you were visiting before you came to our Site and the website you visit after you leave our Site, if this information is supplied to us by your browser.</p>

<p>In many cases, the information we collect using cookies and other tools is used in non-identifiable ways, without any reference to Personal Information. For example, we use information we collect about Site users to optimize our Sites and understand its traffic and usage patterns. In other cases, we associate the information we collect using cookies and related technologies with Personal Information. In that case, this Privacy Policy governs how we use that information.</p>

